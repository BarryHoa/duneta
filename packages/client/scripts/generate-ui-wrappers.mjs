#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const CLIENT_ROOT = path.resolve(__dirname, '..');
const COMPONENTS_DIR = path.join(CLIENT_ROOT, 'components');
const MANIFEST_PATH = path.join(COMPONENTS_DIR, 'manifest.json');

const AUTO_HEADER = '// AUTO-GENERATED by scripts/generate-ui-wrappers.mjs — do not edit.\n';

/** Never regenerate wrapper source for these folders. */
const SKIP_FOLDERS = new Set([
  'DunetaAlertDialog',
  'DunetaDataTable',
  'DunetaLayout',
  'DunetaLink',
  'DunetaLoadError',
  'DunetaSimpleTable',
  'DunetaUpload',
]);

/** Wrapper TSX with hand-maintained logic — skip main component regeneration. */
const CUSTOM_WRAPPERS = new Set(['DunetaPagination', 'DunetaSelect']);
const EXTRA_INDEX_EXPORTS = {
  DunetaToast: [
    "export { showDunetaToast } from './show-toast';",
    "export type { DunetaToastOptions } from './show-toast';",
  ],
  DunetaInput: [
    "export { DunetaDatePicker } from './DunetaDatePicker';",
    "export { DunetaInputEmail } from './DunetaInputEmail';",
    "export { DunetaInputNumber } from './DunetaInputNumber';",
    "export { DunetaInputPassword } from './DunetaInputPassword';",
    "export { DunetaInputPhone } from './DunetaInputPhone';",
    "export { DunetaInputSearch } from './DunetaInputSearch';",
    "export { DunetaTextArea } from './DunetaTextArea';",
  ],
  DunetaForm: [
    "export { DunetaFormField } from './DunetaFormField';",
    "export { DunetaFormRadioField } from './DunetaFormRadioField';",
    "export { DunetaFormSection } from './DunetaFormSection';",
  ],
  DunetaSelect: ["export { DunetaSelectSingle } from './DunetaSelectSingle';"],
};

const HAND_WRITTEN_BARREL = `
export { DunetaLink } from './DunetaLink';
export type { DunetaLinkProps } from './DunetaLink';
export {
  DunetaAlertDialog,
  showDunetaAlertSuccess,
  showDunetaAlertWarning,
  showDunetaAlertError,
  showDunetaConfirm,
  showDunetaLoading,
  runWithDunetaLoading,
} from './DunetaAlertDialog';
export type * from './DunetaAlertDialog';
export { DunetaSimpleTable } from './DunetaSimpleTable';
export type { DunetaSimpleTableProps, DunetaSimpleTableColumn } from './DunetaSimpleTable';
export {
  DunetaDataTable,
  useDunetaDataTable,
} from './DunetaDataTable';
export type * from './DunetaDataTable';
export { DunetaUpload, DunetaUploadFile, DunetaUploadImage, DunetaViewImage } from './DunetaUpload';
export type * from './DunetaUpload';
export { DunetaLoadError } from './DunetaLoadError';
export type { DunetaLoadErrorProps } from './DunetaLoadError';
export { DunetaLayoutSection, DunetaLayoutHeader, DunetaLayoutActions, DunetaLayoutGrid, DunetaFlex, DunetaBox } from './DunetaLayout';
export type * from './DunetaLayout';
`.trim();

function isAutoGenerated(filePath) {
  if (!fs.existsSync(filePath)) return true;
  const content = fs.readFileSync(filePath, 'utf8');
  return content.startsWith(AUTO_HEADER.trim()) || content.includes('AUTO-GENERATED by scripts/generate-ui-wrappers.mjs');
}

function parseParts(typesContent, heroName) {
  const parts = [];
  const re = /export type (\w+) = ComponentProps<typeof (\w+)(?:\.(\w+))?>/g;

  for (const match of typesContent.matchAll(re)) {
    const subPart = match[3] ?? 'Root';
    parts.push({
      propsType: match[1],
      heroName: match[2],
      subPart,
      fnName: subPart === 'Root' ? `${match[1].replace(/Props$/, '')}Root` : match[1].replace(/Props$/, ''),
      exportKey: subPart,
    });
  }

  if (parts.length === 0) {
    parts.push({
      propsType: `${heroName.replace(/^Duneta/, 'Duneta')}Props`.replace('Duneta', 'Duneta'),
      heroName: heroName.replace(/^Duneta/, ''),
      subPart: 'Root',
      fnName: `${heroName}Root`,
      exportKey: 'Root',
    });
  }

  return parts;
}

function heroTag(heroImportName, subPart) {
  return subPart === 'Root' ? heroImportName : `${heroImportName}.${subPart}`;
}

function generateTypes(dunetaName, heroName, parts) {
  const lines = [
    AUTO_HEADER,
    "import type { ComponentProps } from 'react';",
    `import { ${heroName} } from '@heroui/react';`,
    '',
  ];

  for (const part of parts) {
    lines.push(
      `export type ${part.propsType} = ComponentProps<typeof ${heroTag(heroName, part.subPart)}>;`,
    );
  }

  return `${lines.join('\n')}\n`;
}

function generateComponent(dunetaName, heroName, parts) {
  const propsImports = parts.map((p) => p.propsType).join(', ');
  const fns = parts
    .map((part) => {
      const tag = heroTag(heroName, part.subPart);
      return `function ${part.fnName}(props: ${part.propsType}) {
  return <${tag} {...props} />;
}`;
    })
    .join('\n\n');

  const root = parts.find((p) => p.subPart === 'Root') ?? parts[0];
  const assigns = parts
    .map((part) => `  ${part.exportKey}: ${part.fnName},`)
    .join('\n');

  return `${AUTO_HEADER}import { ${heroName} } from '@heroui/react';
import type { ${propsImports} } from './types';

${fns}

export const ${dunetaName} = Object.assign(${root.fnName}, {
${assigns}
});
`;
}

function generateIndex(dunetaName, extraExports = []) {
  const lines = [
    AUTO_HEADER,
    `export { ${dunetaName} } from './${dunetaName}';`,
    ...extraExports,
    "export type * from './types';",
  ];
  return `${lines.join('\n')}\n`;
}

function generateBarrel(manifest) {
  const lines = [AUTO_HEADER];

  for (const entry of manifest) {
    const { dunetaName } = entry;
    lines.push(`export { ${dunetaName}${dunetaName === 'DunetaToast' ? ', showDunetaToast' : ''} } from './${dunetaName}';`);
    lines.push(`export type * from './${dunetaName}';`);
  }

  lines.push(HAND_WRITTEN_BARREL);
  return `${lines.join('\n')}\n`;
}

function loadPartsFromTypes(folderPath, dunetaName, heroName) {
  const typesPath = path.join(folderPath, 'types.ts');
  if (fs.existsSync(typesPath)) {
    return parseParts(fs.readFileSync(typesPath, 'utf8'), dunetaName);
  }

  return [
    {
      propsType: `${dunetaName}Props`,
      heroName,
      subPart: 'Root',
      fnName: `${dunetaName}Root`,
      exportKey: 'Root',
    },
  ];
}

function generateWrapper(entry, { force = false } = {}) {
  const { folder, heroName, dunetaName } = entry;
  if (SKIP_FOLDERS.has(folder)) return;

  const folderPath = path.join(COMPONENTS_DIR, folder);
  const mainPath = path.join(folderPath, `${dunetaName}.tsx`);
  const typesPath = path.join(folderPath, 'types.ts');
  const indexPath = path.join(folderPath, 'index.ts');

  if (CUSTOM_WRAPPERS.has(folder)) {
    if (force || !fs.existsSync(indexPath) || isAutoGenerated(indexPath)) {
      fs.writeFileSync(indexPath, generateIndex(dunetaName, EXTRA_INDEX_EXPORTS[folder] ?? []));
    }
    return;
  }

  if (!force && fs.existsSync(mainPath) && !isAutoGenerated(mainPath)) {
    return;
  }

  const parts = loadPartsFromTypes(folderPath, dunetaName, heroName);
  const typesSource = generateTypes(dunetaName, heroName, parts);

  if (force || !fs.existsSync(typesPath) || isAutoGenerated(typesPath)) {
    fs.writeFileSync(typesPath, typesSource);
  }

  fs.writeFileSync(mainPath, generateComponent(dunetaName, heroName, parts));
  fs.writeFileSync(indexPath, generateIndex(dunetaName, EXTRA_INDEX_EXPORTS[folder] ?? []));
}

function main() {
  const force = process.argv.includes('--force');
  const manifest = JSON.parse(fs.readFileSync(MANIFEST_PATH, 'utf8'));

  for (const entry of manifest) {
    generateWrapper(entry, { force });
  }

  fs.writeFileSync(path.join(COMPONENTS_DIR, 'index.ts'), generateBarrel(manifest));
  console.log(`Generated ${manifest.length} HeroUI wrappers → components/`);
}

main();
